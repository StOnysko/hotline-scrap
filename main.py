# Функція для генерації URL шляху
def to_model_path(model, color, storage):
    base = "apple"
    model_part = model.lower().replace(" ", "-")
    storage_part = f"{storage}gb"  # storage вже є числом, перетворюємо в рядок і додаємо "gb"
    color_part = color.lower().replace(" ", "-").replace("product-red", "product-red")  # вже виправлений
    return f"{base}-{model_part}-{storage_part}-{color_part}"


# Підключення до API
url = 'https://hotline.ua/svc/frontend-api/graphql'

# Створюємо спільний CSV файл
with open("iphone_al_models_price_chart.csv", mode='w', newline='', encoding='utf-8') as file:
    writer = csv.writer(file)
    writer.writerow(['ID Моделі', 'Дата', 'Ціна (UAH)', 'Ціна (USD)'])

    for model, color, storage in iphone_combinations:
        model_path = to_model_path(model, color, storage)
        headers = {
            'Content-Type': 'application/json',
            'Accept': '*/*',
            'Sec-Fetch-Site': 'same-origin',
            'Accept-Language': 'en-GB,en;q=0.9',
            'Sec-Fetch-Mode': 'cors',
            'Origin': 'https://hotline.ua',
            'User-Agent': 'Mozilla/5.0',
            'Referer': f'https://hotline.ua/ua/mobile-mobilnye-telefony-i-smartfony/{model_path}/?tab=about',
            'Accept-Encoding': 'gzip, deflate, br',
            'Sec-Fetch-Dest': 'empty',
            'x-language': 'uk',
            'Priority': 'u=3, i',
            'x-token': '6b22230c-6222-4a1b-a585-f023c189bfeb',
            'x-request-id': '5585a43877a8725f99b6f66eccd082ae'
        }

        payload = {
            "operationName": "getChart",
            "variables": {
                "path": model_path
            },
            "query": """
            query getChart($path: String!) {
              chart(productPath: $path) {
                id
                priceUAH
                priceUSD
                popularity
                quantity
                __typename
              }
            }
            """
        }

        response = requests.post(url, headers=headers, json=payload)

        if response.status_code == 200:
            data = response.json()
            chart = data.get("data", {}).get("chart", {})
            price_uah = chart.get("priceUAH", [])
            price_usd = chart.get("priceUSD", [])

            for i in range(len(price_uah)):
                writer.writerow([
                    model_path,
                    price_uah[i][0],
                    price_uah[i][1],
                    price_usd[i][1] if i < len(price_usd) else 'N/A'
                ])
            print(f"✅ Додано: {model_path}")
        else:
            print(f"❌ Помилка: {response.status_code} для {model_path}")